"""
agents/vss_glue_agent.py
═══════════════════════════════════════════════════════════════
VSS Glue Agent — runs AFTER all 7 domain agents complete.

Architecture:
  [7 domain agents] → VehicleHalService<Domain>.h + VehicleService<Domain>.cpp
          ↓
  [VssGlueAgent] → aggregator service binary wrapping all domains
          ↓
  [android.hardware.automotive.vehicle@V3-vss-service] → runs on device

Generated artifacts (all correct, no post-processing needed):
  VssVehicleHardware.h      — IVehicleHardware subclass (aggregator)
  VssVehicleHardware.cpp    — aggregates getAllPropertyConfigs() from 7 domains
  VssVehicleService.cpp     — AIDL V3 main() entry point
  Android.bp                — builds all 7 domain .cpp + aggregator into 1 binary
  manifest_vss.xml          — VINTF manifest
  *.rc                      — init.rc service definition
  vehicle_hal_vss.te        — SELinux policy

Why deterministic (not LLM)?
  These artifacts depend on exact Android 14 API contracts and cross-domain
  consistency. LLM cannot guarantee correct aggregation across 7 domains.
  Property configs are parsed from LLM-generated AIDL files (correct IDs).
═══════════════════════════════════════════════════════════════
"""

from __future__ import annotations

import glob
import logging
import os
import re
from typing import Optional

logger = logging.getLogger(__name__)

BINARY_NAME  = "android.hardware.automotive.vehicle@V3-vss-service"
SERVICE_NAME = "vendor.vehicle-hal-vss"
INSTANCE     = "android.hardware.automotive.vehicle.IVehicle/default"

DOMAINS = ["adas", "body", "cabin", "chassis", "hvac", "infotainment", "powertrain"]

# AAOS 32-bit prop ID: (group<<28) | (area<<20) | (type<<16) | index
# area=0x0 (GLOBAL), group per domain
DOMAIN_GROUP = {
    "adas":         0x2,
    "powertrain":   0x3,
    "body":         0x4,
    "cabin":        0x5,
    "chassis":      0x6,
    "hvac":         0x7,
    "infotainment": 0x8,  # separate from hvac to avoid prop ID collision
}

VHAL_TYPE_BITS = {
    "BOOLEAN": 0x21,
    "INT":     0x14,
    "INT32":   0x14,
    "FLOAT":   0x16,
    "STRING":  0x00,
    "BYTES":   0x00,
    "INT64":   0x15,
}


def _encode_prop_id(group: int, vtype: str, index: int) -> str:
    """Encode AAOS 32-bit property ID: (group<<28)|(area<<20)|(type<<16)|index."""
    type_bits = VHAL_TYPE_BITS.get(vtype.upper(), 0x14)
    prop_id = (group << 28) | (0x0 << 20) | (type_bits << 16) | (index & 0xFFFF)
    return hex(prop_id)


def _domain_from_filename(filename: str) -> str:
    """VehiclePropertyAdas.aidl → adas"""
    base = os.path.basename(filename)
    base = base.replace("VehicleProperty", "").replace(".aidl", "")
    return base.lower()


def _parse_aidl_properties(aidl_dir: str) -> list[dict]:
    """
    Parse property configs from LLM-generated AIDL enum files.
    Re-encodes bare 16-bit IDs into correct AAOS 32-bit format.
    Deduplicates by (domain, index).
    """
    props = []
    seen_ids: set[str] = set()

    # Match enum member lines: 4-space indent, NAME = 0xHEX, // TYPE, ACCESS
    pattern = re.compile(
        r"^\s{4}(\w+)\s*=\s*(0x[0-9a-fA-F]+|\d+)"
        r"(?:\s*,\s*//\s*(BOOLEAN|INT|INT32|FLOAT|STRING|BYTES|INT64)"
        r"(?:,\s*(READ|WRITE|READ_WRITE))?)?",
        re.MULTILINE,
    )

    SKIP = {"package", "enum", "interface", "parcelable", "import"}

    aidl_files = glob.glob(os.path.join(aidl_dir, "VehicleProperty*.aidl"))
    for f in sorted(aidl_files):
        domain = _domain_from_filename(f)
        group  = DOMAIN_GROUP.get(domain, 0x1)
        content = open(f, errors="ignore").read()
        index = 0
        for m in pattern.finditer(content):
            name = m.group(1)
            if name in SKIP:
                continue
            vtype  = (m.group(3) or "INT").replace("INT32", "INT")
            access = m.group(4) or "READ_WRITE"
            prop_id = _encode_prop_id(group, vtype, index)

            dedup_key = f"{domain}:{index}"
            if dedup_key in seen_ids:
                continue
            seen_ids.add(dedup_key)

            props.append({
                "name":    name,
                "prop_id": prop_id,
                "type":    vtype,
                "access":  access,
                "domain":  domain,
            })
            index += 1

    logger.info(f"[VssGlueAgent] Parsed {len(props)} properties from {len(aidl_files)} AIDL files")
    return props


