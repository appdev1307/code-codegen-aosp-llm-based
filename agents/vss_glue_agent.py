class VssGlueAgent:
    """
    Generates deterministic Android 14 VHAL glue artifacts.
    Must run AFTER all domain agents have written their AIDL files.

    getAllPropertyConfigs() is populated by parsing LLM-generated AIDL
    enum files — no LLM calls needed here.

    Note on VehicleHalService*.cpp:
    These LLM-generated per-domain files each define VssVehicleHardware
    with duplicate symbols — they cannot be compiled together.
    The VssGlueAgent generates a unified VssVehicleHardware.cpp instead,
    with property configs aggregated from AIDL parsing.
    """

    def run(self, output_dir: str, aidl_dir: Optional[str] = None) -> dict[str, str]:
        os.makedirs(output_dir, exist_ok=True)

        # Parse real properties from AIDL files generated by pipeline
        props = []
        if aidl_dir and os.path.isdir(aidl_dir):
            props = _parse_aidl_properties(aidl_dir)
            logger.info(f"[VssGlueAgent] Using {len(props)} real VSS properties")
        else:
            logger.warning("[VssGlueAgent] No aidl_dir — getAllPropertyConfigs() returns empty")

        artifacts = {
            "VssVehicleHardware.h":   _generate_vss_hardware_h(),
            "VssVehicleHardware.cpp": _generate_vss_hardware_cpp(props),
            "VssVehicleService.cpp": _generate_service_main(),
            "Android.bp":             _generate_android_bp(output_dir),
            f"{BINARY_NAME}.rc":      _generate_init_rc(),
            "manifest_vss.xml":       _generate_manifest(),
            "vehicle_hal_vss.te":     _generate_vss_te(),
        }

        for filename, content in artifacts.items():
            with open(os.path.join(output_dir, filename), "w") as f:
                f.write(content)

        prop_count = len(props)
        logger.info(f"[VssGlueAgent] {len(artifacts)} artifacts, {prop_count} properties → {output_dir}")
        print(f"  [VssGlueAgent] ✓ {len(artifacts)} artifacts, {prop_count} VSS properties in getAllPropertyConfigs()")
        return artifacts

    def validate(self, output_dir: str) -> bool:
        required = ["VssVehicleHardware.h", "VssVehicleHardware.cpp",
                    "VssVehicleService.cpp", "Android.bp",
                    f"{BINARY_NAME}.rc", "manifest_vss.xml"]
        missing = [f for f in required if not os.path.exists(os.path.join(output_dir, f))]
        if missing:
            logger.error(f"[VssGlueAgent] Missing: {missing}")
            return False
        return True