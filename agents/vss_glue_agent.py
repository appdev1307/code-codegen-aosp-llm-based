DOMAINS = ["adas", "body", "cabin", "chassis", "hvac", "infotainment", "powertrain"]

_VSS_GROUP = 0x20000000
_VSS_AREA  = 0x01000000
_GLOBAL_IDX_START = 0x1000

_TYPE_BITS = {
    "BOOLEAN": 0x00200000,
    "INT":     0x00400000,
    "FLOAT":   0x00600000,
    "STRING":  0x00100000,
    "BYTES":   0x00700000,
    "INT64":   0x00500000,
}
_TYPE_DEFAULT = 0x00e00000


def _build_full_prop_id(global_idx: int, vtype: str) -> int:
    type_bits = _TYPE_BITS.get(vtype, _TYPE_DEFAULT)
    return _VSS_GROUP | _VSS_AREA | type_bits | (global_idx & 0xFFFF)


def _parse_aidl_properties(aidl_dir: str, file_pattern: str = "VehicleProperty*.aidl") -> list[dict]:
    props = []
    pattern = re.compile(
        r"^\s+(\w+)\s*=\s*(0x[0-9a-fA-F]+|\d+),\s*//\s*(\w+)(?:,\s*(\w+))?",
        re.MULTILINE,
    )

    aidl_files = sorted(glob.glob(os.path.join(aidl_dir, file_pattern)))
    global_idx = _GLOBAL_IDX_START

    _TYPE_KEYWORDS = ["BOOLEAN", "FLOAT", "STRING", "BYTES", "INT64", "INT"]

    for f in aidl_files:
        content = open(f, errors="ignore").read()
        for m in pattern.finditer(content):
            name = m.group(1)
            # Find type keyword anywhere in comment — handles any order
            comment_text = ((m.group(3) or "") + " " + (m.group(4) or "")).upper()
            # Also check raw line after //
            raw_after = content[m.end():m.end()+50]
            comment_match = re.search(r",\s*(\w+)\s*$", raw_after.split("\n")[0])
            if comment_match:
                comment_text += " " + comment_match.group(1).upper()
            vtype = "INT"
            for tk in _TYPE_KEYWORDS:
                if tk in comment_text:
                    vtype = tk
                    break
            access = "READ_WRITE" if "READ_WRITE" in comment_text else "READ"
            full_id = _build_full_prop_id(global_idx, vtype)
            props.append({
                "name": name,
                "prop_id": hex(full_id),
                "type": vtype,
                "access": access
            })
            global_idx += 1

    logger.info(f"[VssGlueAgent] Parsed {len(props)} properties from {len(aidl_files)} AIDL files")
    return props


def get_aidl_property_names(aidl_dir: str, file_pattern: str = "VehicleProperty*.aidl") -> set:
    """
    Return the set of constant names defined across every AIDL enum
    file under `aidl_dir`.

    Thin wrapper around `_parse_aidl_properties()` — the proven,
    already-deterministic parser this module uses to build the
    aggregator (`mPropIds`) and VTS (`kVssPropertyIds`) lists.

    Exists so C4's post-validation consistency check
    (dspy_opt/validators.py::check_cpp_aidl_name_consistency) can
    verify "does the CPP agent's output use only names that exist in
    AIDL" against the SAME name set the aggregator/VTS already trust,
    instead of re-implementing enum-parsing regex a second time.
    Per project invariant: prefer existing proven mechanisms over
    inventing parallel solutions.

    C4-only usage — C1/C2/C3 do not call this.
    """
    if not aidl_dir or not os.path.isdir(aidl_dir):
        return set()
    return {p["name"] for p in _parse_aidl_properties(aidl_dir, file_pattern)}


