def _parse_class_name(header_content: str) -> str:
    """Parse class name from generated header — works regardless of LLM naming."""
    m = re.search(r'class\s+(\w+)\s*:\s*public\s+IVehicleHardware', header_content)
    return m.group(1) if m else None


def _generate_vss_hardware_cpp(props: list[dict], domains: list[str] = None,
                                domain_headers: dict[str, str] = None) -> str:
    """VssVehicleHardware holds array of IVehicleHardware domain services.
    Class names are parsed from generated headers — LLM naming does not matter.
    Fallback: use AIDL-parsed prop IDs if domain services return empty.

    Uses the unified VehicleProperty.h (merged from all domain enums into
    aidl_property/VehicleProperty.aidl on GCP VM) instead of per-domain
    VehiclePropertyAdas.h / VehiclePropertyBody.h etc., so this file compiles
    correctly after merge_vehicle_property.py has been applied to AOSP.
    """
    init_lines = "\n    ".join(f"mPropIds.push_back({p['prop_id']});" for p in props)

    _domains = domains if domains else DOMAINS
    _headers = domain_headers or {}

    # Parse actual class names from generated headers
    domain_class_map = {}
    for d in _domains:
        hdr = _headers.get(d, "")
        cls = _parse_class_name(hdr) if hdr else f"VehicleHalService{d.capitalize()}"
        domain_class_map[d] = cls

    domain_includes = "\n".join(
        f'#include "VehicleHalService{d.capitalize()}.h"' for d in _domains
    )

    # Direct instantiation and aggregate — class name agnostic
    domain_configs = "\n    ".join(
        f'{{ {cls} svc; auto c = svc.getAllPropertyConfigs(); configs.insert(configs.end(), c.begin(), c.end()); }}'
        for d, cls in domain_class_map.items()
    )

    return f"""// Generated by VssGlueAgent — DO NOT EDIT
// Aggregates domain VehicleHalService* implementations (each extends IVehicleHardware)
#include "VssVehicleHardware.h"
{domain_includes}
// Unified property enum — generated from merged aidl_property/VehicleProperty.aidl
// (all domain enums merged into VehicleProperty via merge_vehicle_property.py)
#include <aidl/android/hardware/automotive/vehicle/VehicleProperty.h>
#include <android-base/logging.h>

namespace android::hardware::automotive::vehicle {{
using namespace aidl::android::hardware::automotive::vehicle;

VssVehicleHardware::VssVehicleHardware() {{
    mPropIds.reserve({len(props)});
    {init_lines}
    LOG(INFO) << "[VSS] Loaded " << mPropIds.size() << " VSS properties";
}}

static VehiclePropConfig makeConfig(int32_t propId) {{
    VehiclePropConfig cfg;
    cfg.prop = propId;
    cfg.access = VehiclePropertyAccess::READ_WRITE;
    cfg.changeMode = VehiclePropertyChangeMode::ON_CHANGE;
    cfg.areaConfigs = {{ {{.areaId = 0}} }};
    return cfg;
}}

std::vector<VehiclePropConfig> VssVehicleHardware::getAllPropertyConfigs() const {{
    std::vector<VehiclePropConfig> configs;
    // Aggregate from domain services
    {domain_configs}
    // If domain services returned raw 16-bit IDs, use deterministic mPropIds instead
    bool has_invalid = false;
    for (const auto& cfg : configs) {{
        if ((static_cast<uint32_t>(cfg.prop) & 0xF0000000u) == 0) {{
            has_invalid = true;
            break;
        }}
    }}
    if (configs.empty() || has_invalid) {{
        configs.clear();
        for (int32_t id : mPropIds) configs.push_back(makeConfig(id));
    }}
    LOG(INFO) << "[VSS] Registered " << configs.size() << " VSS properties";
    return configs;
}}

StatusCode VssVehicleHardware::getPropConfigs(
        const std::vector<int32_t>& props,
        std::vector<VehiclePropConfig>* outConfigs) {{
    outConfigs->clear();
    for (int32_t p : props) outConfigs->push_back(makeConfig(p));
    return StatusCode::OK;
}}

StatusCode VssVehicleHardware::setValues(
        std::shared_ptr<const SetValuesCallback> callback,
        const std::vector<SetValueRequest>& requests) {{
    std::vector<SetValueResult> results;
    for (const auto& r : requests) {{
        mStore[r.value.prop] = r.value;
        results.push_back({{.requestId = r.requestId, .status = StatusCode::OK}});
    }}
    (*callback)(results);
    return StatusCode::OK;
}}

StatusCode VssVehicleHardware::getValues(
        std::shared_ptr<const GetValuesCallback> callback,
        const std::vector<GetValueRequest>& requests) const {{
    std::vector<GetValueResult> results;
    for (const auto& r : requests) {{
        auto it = mStore.find(r.prop.prop);
        if (it != mStore.end()) {{
            results.push_back({{.requestId = r.requestId,
                               .status = StatusCode::OK,
                               .prop = it->second}});
        }} else {{
            VehiclePropValue val;
            val.prop = r.prop.prop;
            val.areaId = r.prop.areaId;
            val.value.int32Values = {{0}};
            results.push_back({{.requestId = r.requestId,
                               .status = StatusCode::OK,
                               .prop = val}});
        }}
    }}
    (*callback)(results);
    return StatusCode::OK;
}}

DumpResult VssVehicleHardware::dump(const std::vector<std::string>&) {{ return {{}}; }}
StatusCode VssVehicleHardware::checkHealth() {{ return StatusCode::OK; }}
void VssVehicleHardware::registerOnPropertyChangeEvent(std::unique_ptr<const PropertyChangeCallback>) {{}}
void VssVehicleHardware::registerOnPropertySetErrorEvent(std::unique_ptr<const PropertySetErrorCallback>) {{}}
}} // namespace
"""


