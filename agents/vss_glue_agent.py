class VssGlueAgent:
    """
    Deterministic Android 14 VHAL glue — runs AFTER all 7 domain agents.

    Generates a complete, correct service binary that wraps all domain outputs.
    No post-processing by apply_aosp14_fixes.sh required for these artifacts.

    apply_aosp14_fixes.sh responsibility:
      - Copy these files into AOSP tree
      - Platform-level fixes (AIDL freeze, FCM, device.mk, SELinux file_contexts)
      - NOT rewrite anything generated here
    """

    def run(self, output_dir: str, aidl_dir: Optional[str] = None) -> dict[str, str]:
        os.makedirs(output_dir, exist_ok=True)

        props = []
        if aidl_dir and os.path.isdir(aidl_dir):
            props = _parse_aidl_properties(aidl_dir)
            logger.info(f"[VssGlueAgent] {len(props)} VSS properties parsed")
        else:
            logger.warning("[VssGlueAgent] No aidl_dir provided — getAllPropertyConfigs() will be empty")

        artifacts = {
            "VssVehicleHardware.h":   _generate_vss_hardware_h(),
            "VssVehicleHardware.cpp": _generate_vss_hardware_cpp(props),
            "VssVehicleService.cpp":  _generate_vss_service_cpp(),
            "Android.bp":             _generate_android_bp(),
            f"{BINARY_NAME}.rc":      _generate_init_rc(),
            "manifest_vss.xml":       _generate_manifest(),
            "vehicle_hal_vss.te":     _generate_vss_te(),
        }

        for filename, content in artifacts.items():
            with open(os.path.join(output_dir, filename), "w") as f:
                f.write(content)

        print(f"  [VssGlueAgent] ✓ {len(artifacts)} artifacts, {len(props)} VSS properties → {output_dir}")
        logger.info(f"[VssGlueAgent] Done: {len(artifacts)} files, {len(props)} props")
        return artifacts

    def validate(self, output_dir: str) -> bool:
        required = [
            "VssVehicleHardware.h",
            "VssVehicleHardware.cpp",
            "VssVehicleService.cpp",
            "Android.bp",
            f"{BINARY_NAME}.rc",
            "manifest_vss.xml",
            "vehicle_hal_vss.te",
        ]
        missing = [f for f in required if not os.path.exists(os.path.join(output_dir, f))]
        if missing:
            logger.error(f"[VssGlueAgent] Missing: {missing}")
            return False
        return True