"""
agents/vss_glue_agent.py
═══════════════════════════════════════════════════════════════
VSS Glue Agent — runs AFTER all domain agents complete.

Reads AIDL enum files and VehicleHalService*.cpp generated by
the pipeline, then produces:
  - VssVehicleHardware.h    — Android 14 IVehicleHardware subclass
  - VssVehicleHardware.cpp  — aggregates getAllPropertyConfigs()
                               from all domain implementations
  - VehicleServiceMain.cpp  — AIDL V3 service entry point
  - Android.bp              — compiles VssVehicleHardware +
                               all VehicleHalService*.cpp
  - manifest_vss.xml        — VINTF with <interface> tags
  - *.rc                    — init.rc with correct binary name

Why deterministic (not LLM)?
  These artifacts depend on exact Android 14 API contracts.
  VehicleHalService*.cpp property configs are parsed from
  LLM-generated AIDL files to populate getAllPropertyConfigs().
═══════════════════════════════════════════════════════════════
"""

